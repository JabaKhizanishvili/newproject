You can use business leave application when you have to leave office area during worktime for several reasons( e.g. meetings). You can register application in advance. <br><br>
	<strong> For employee business trip registration  :</strong><br><br>
	<span class='bullet'></span> Choose employee <br>
	<span class='bullet'></span> Choose date<br>
	<span class='bullet'></span> Choose leave Start and End hours <br>
	<span class='bullet'></span> Write business leave reason<br>
	<span class='bullet'></span> Click on button "Save and Close" <br><br>
	Then system will take you to the history page, where you can confirm the registered applications.<br>
	After confirming, the employee will receive an SMS message that his application has been approved.