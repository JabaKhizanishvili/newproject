<strong>To register an employee's absence:</strong><br><br>
<span class='bullet'></span> Select the employee<br>
<span class='bullet'></span> Select the date when the employee was absent<br>
<span class='bullet'></span> Specify the reason for the absence<br>
<span class='bullet'></span> Click on button "Save & Close" button<br><br>
After that, the system will take you to the history page, where you can confirm the application of the employee.