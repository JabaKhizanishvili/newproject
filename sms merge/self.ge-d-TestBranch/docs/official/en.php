Employee's hourly leave is an application that allows us to write an official or honorable leave to the employee for the current day.<br><br>
<strong>For register the hourly leave:</strong><br><br>
<span class='bullet'></span> Select the employee<br>
<span class='bullet'></span> Select the reason why you are registering the the hourly leave for the employee<br>
<span class='bullet'></span> Optionally, you can leave a comment<br>
<span class='bullet'></span> Click on button "Save & Close" </span><br><br>

The employee receives an SMS message about the approval of the hourly leave.<br>
