<strong>To register an employee's vacation:</strong><br><br>
<span class='bullet'></span> Select the employee</span> <br>
<span class='bullet'></span> Select the type of vacation</span> <br>
<span class='bullet'></span> Select vacation Start and End date </span> <br>
<span class='bullet'></span> Click on button "Save & Close" button</span> <br><br>
After that, the system will take you to the history page, where you can confirm the application of the employee.<br>
After confirming the application, the employee will receive an SMS  that the leave has been approved. 