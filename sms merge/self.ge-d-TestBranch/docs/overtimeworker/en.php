 <strong>For employee ovetime registration:</strong><br><br>
<span class='bullet'></span> Choose employee <br>
<span class='bullet'></span> Choose overtime date (You can register overtime only by past date )<br>
<span class='bullet'></span> Write overtime duration (hours) <br>
<span class='bullet'></span> Write overtime reason <br>
<span class='bullet'></span> Click on button "Save & Close" <br><br>
 Then system will take you to the history page, where you can confirm the registered applications.<br>
 After confirming, the employee will receive an SMS that his application has been approved.