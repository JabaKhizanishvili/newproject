<strong>For employee bulletin registration:</strong><br><br>
<span class='bullet'></span> Select employee<br>
<span class='bullet'></span> Select the status of the application: current, closed or confirmed<br>
<span class='bullet'></span> Select the date from when the employee goes to the bulletin (you can specify the current, future or past day as the date of opening the bulletin))<br>
<span class='bullet'></span> Specify the number of days<br>
<span class='bullet'></span> Optionally, write a comment<br>
<span class='bullet'></span> Upload the file<br>
<span class='bullet'></span> Click on button "Save & Close" <br><br>

 After that, your application is sent to the supervisor for approval.<br>
 You can specify the current, future or past day as the opening date of the bulletin.<br>